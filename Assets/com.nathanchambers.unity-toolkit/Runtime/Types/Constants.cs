namespace Toolkit {
    public static class Constants {
        public const int TIME_SECOND = 1;
        public const int TIME_MINUTE = TIME_SECOND * 60;
        public const int TIME_HOUR = TIME_MINUTE * 60;
        public const int TIME_DAY = TIME_HOUR * 24;
        public const int TIME_WEEK = TIME_DAY * 7;
        public const int TIME_YEAR = TIME_WEEK * 52;
    }
}